## 收集的資料

我們收集以下資料以提供服務：
- 帳號資料：電子郵件、用戶名稱、個人頭像
- 換卡資料：持有卡牌清單、想換卡牌清單、換卡歷史記錄
- 收藏資料：您主動新增至收藏庫的卡牌資訊
- 互動資料：文章、留言、換卡評價
- 裝置資料：推播通知訂閱端點（用於發送通知，不用於識別個人）

## 資料使用方式

收集的資料僅用於：
- 提供換卡配對、訊息傳遞等核心服務
- 發送您訂閱的換卡配對通知與系統通知
- 改善平台功能與用戶體驗
- 維護平台安全與防止濫用

## 資料分享

我們不會將您的個人資料出售或出租給第三方。以下情況除外：
- 您明確同意分享
- 依法律要求配合政府機關
- 保護本平台或其他用戶的安全

## 資料安全

本平台使用業界標準加密技術（TLS/HTTPS）保護傳輸中的資料，並採用 Supabase 提供的安全資料庫基礎設施，設有行列級安全政策（RLS）防止未授權存取。

## Cookie 與本地儲存

本平台使用 Cookie 維持登入狀態，並使用 localStorage 儲存部分用戶偏好設定（如通知提示是否已關閉）。

